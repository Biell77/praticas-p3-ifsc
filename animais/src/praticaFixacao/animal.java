package praticaFixacao;

public class animal {
	
	private String Nome;
	private String Raça;
	private String Cor;
	private String ecossistema;
	private float Comprimento;
	private Integer NumeroDePatas;
	
	public void Caminha() {
		
	}
	
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getRaça() {
		return Raça;
	}
	public void setRaça(String raça) {
		Raça = raça;
	}
	public String getCor() {
		return Cor;
	}
	public void setCor(String cor) {
		Cor = cor;
	}
	public String getEcossistema() {
		return ecossistema;
	}
	public void setEcossistema(String ecossistema) {
		this.ecossistema = ecossistema;
	}
	public float getComprimento() {
		return Comprimento;
	}
	public void setComprimento(float comprimento) {
		Comprimento = comprimento;
	}
	public Integer getNumeroDePatas() {
		return NumeroDePatas;
	}
	public void setNumeroDePatas(Integer numeroDePatas) {
		NumeroDePatas = numeroDePatas;
	}

	

}
